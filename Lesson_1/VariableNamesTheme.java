public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("\n1. Разные переменные");
        // Цифра
        int digit = 1;
        System.out.println("Эта цифра = " + digit);        
        // Сумма чисел
        int numberSum = 11;
        System.out.println("Сумма чисел = " + numberSum);
        // Произведение цифр
        int productOfDigits = 12;
        System.out.println("Произведение цифр = " + productOfDigits);
        // Максимальное число
        int maxNumber = 99;
        System.out.println("Максимальное число = " + maxNumber);
        // Количество десятков
        int numberOfTens = 4;
        System.out.println("Количество десятков = " + numberOfTens);
        // Вес собаки
        int dogWeight = 33;
        System.out.println("Вес собаки = " + dogWeight);
        // Исходное число
        int initialNumber = 10;
        System.out.println("Исходное число = " + initialNumber);
        // Процент по вкладу
        int interestRate = 70;
        System.out.println("Процент по вкладу = " + interestRate);
        // Переменная хранит символ &
        char ampersand = '&';
        System.out.println("Переменная хранит символ = " + ampersand);
        // Число нулей
        int numberOfZeros = 3;
        System.out.println("Число нулей = " + numberOfZeros);
        // Уникальное число
        int uniqueNumber = 777;
        System.out.println("Уникальное число = " + uniqueNumber);
        // Случайное число
        int randomNumber = 5;
        System.out.println("Случайное число = " + randomNumber);
        // Математическое выражение
        int num1 = 2;
        int num2 = 6;
        int mathExpression = num1 + num2;
        System.out.println("Математическое выражение - " + num1 + " + " + num2 + " = " + mathExpression);
        // Счет в игре
        int gameScore = 1;
        System.out.println("Счет в игре = " + gameScore + ":" + gameScore * 2);
        // Максимальная длина
        int maxLength = 10;
        System.out.println("Максимальная длина = " + maxLength);
        // Пункт меню
        int menuItem = 8;
        System.out.println("Пункт меню №" + menuItem);
        // Стоимость кофе
        double coffeeCost = 0.40;
        System.out.println("Стоимость кофе - " + coffeeCost);
        // Начальная дата
        int startDate = 14;
        System.out.println("Начальная дата = " + startDate);
        // Окончание диапазона
        int rangeEnd = 20;
        System.out.println("Окончание диапазона = " + rangeEnd);
        // Имя работника месяца
        char employeeOfTheMonth = 'D';
        System.out.println("Имя работника месяца - " + employeeOfTheMonth + "onald");
        // Название электронной книги
        char eBookTitle = 'H';
        System.out.println("Название электронной книги - " + eBookTitle + "P");
        // Размер
        int size = 100;
        System.out.println("Размер = " + size + "мм");
        // Вместимость
        double capacity = 2.5;
        System.out.println("Вместимость = " + capacity + "l");
        // Счетчик
        int counter = 23;
        System.out.println("Счетчик " + counter + ":" + counter + ":" + counter);
        // Путь до файла
        char filePath = '/';
        System.out.println("Путь до файла " + "D:" + filePath + "StartJava" + filePath);
        // Количество чисел в каждой строке
        int numbersPerLine = 18;
        System.out.println("Количество чисел в каждой строке = " + numbersPerLine);

        System.out.println("\n2. Boolean-переменные");
        // Сотни равны?
        boolean isHundredsMatch = true;
        System.out.println("Сотни равны? " + isHundredsMatch);
        // Компьютер включен?
        boolean isComputerOn = true;
        System.out.println("Компьютер включен? " + isComputerOn);
        // Есть равные цифры?
        boolean hasEqualDigits = true;
        System.out.println("Есть равные цифры? " + hasEqualDigits);
        // Создано?
        boolean isCreated = true;
        System.out.println("Создано? " + isCreated);
        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? " + isEmpty);
        // Активное?
        boolean isActive = true;
        System.out.println("Активное? " + isActive);
        // Новое?
        boolean isNew = true;
        System.out.println("Новое? " + isNew);
        // Электронная почта действительная?
        boolean isEmailValid = false;
        System.out.println("Электронная почта действительная? " + isEmailValid);
        // Имеются уникальные строки?
        boolean hasUniqueStrings = false;
        System.out.println("Имеются уникальные строки? " + hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры");
        // Старый universally unique identifier
        long oldUuid = 446655440000L;
        System.out.println("Старый universally unique identifier: " + oldUuid);
        // Производитель оперативной памяти
        int ramManufacturer = 5;
        System.out.println("Производитель оперативной памяти - Gigabyte DDR" + ramManufacturer);
        // Емкость жесткого диска
        int hddCapacity = 1024;
        System.out.println("Емкость жесткого диска - " + hddCapacity);
        // Протокол передачи гипертекста
        Double httpProtocol = 1.1;
        System.out.println("Протокол передачи гипертекста: HTTP/" + httpProtocol);
        // Сокращенный uniform resource locator
        char shortUrl = '/';
        System.out.println("Сокращенный URL: https:" + shortUrl + shortUrl + "javaops.ru");
        // Новый идентификатор клиента
        int newClientId = 12345;
        System.out.println("Новый идентификатор клиента: " + newClientId);
        // American standard code for information interchange
        char asciiCode = 'A';
        System.out.println("ASCII Code: " + asciiCode);
    }
}